# Parametro es una variable definida entre los parentesisde la funcion
# Argumento (args) es el valor enviado a la funcion
def funcion_args(nombre,apellido):
    print('El nombre recibido es:', nombre)
    print('El apellido recibido es:', apellido)
    
funcion_args('Daniel', 'Romero')
funcion_args('Jonathan', 'Aguirrre')